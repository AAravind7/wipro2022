class Demo
{
	public static void main(String [] args)
	{
		// Arithmetical + - * / %
		int fnum,snum;
		int result; // Local Variable
		
		fnum = 125;
		snum = 25;
		
		result = fnum + snum;
		System.out.println(result);
		
		result = fnum - snum;
		System.out.println(result);
		
		result = fnum * snum;
		System.out.println(result);
		
		result = fnum / snum;
		System.out.println(result);
		
		result = fnum % snum;
		System.out.println(result);
	}
}
